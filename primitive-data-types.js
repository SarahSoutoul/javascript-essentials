// Primitive data types

// Booleans

var boolean = true;
var boolean_false = false

// Numbers - integers or floats
var integer = 3;
var float = 3.4;

// Strings
var string = "Hello, World";

// Single quotation vs double quotation
var brokenString = 'I"m" a broken string'

// Escape character
var brokenString = 'I\'m a \"quote\" broken string'

// Template literals
var brokenString = `I'm ${3 + 3} a broken "quote" string`

// Absence of value

// Null
let x = null;

// Undefined
var y;
console.log(y)
